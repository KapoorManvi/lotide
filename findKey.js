//Finds a key with a given value specified within the callback function
const findKey = function(object, callback) {
  //Loops through keys within passed object
for (let item of Object.keys(object)) {
  //If given value in callback found, return associated key
  if (callback(object[item])) {
    return item;
  }
}
}

